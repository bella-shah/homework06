package hw06;


public class Program18Sum {

    public static void main(String[] args) {
        int a = 125;//local variable
        int b = 24;//local variable
        System.out.println("Expected Output: ");
        System.out.println("125 + " + "24 = " + (a + b));//149
        System.out.println("125 - " + "24 = " + (a - b));//101
        System.out.println("125 x " + "24 = " + (a * b));//3000
        System.out.println("125 / " + "24 = " + (a / b));//5
        System.out.println("125 mod " + "24 = " + (a % b));//5
    }
}




